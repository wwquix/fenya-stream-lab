export function formatCategory(category, t) {
  return t.categoryLabels?.[category] ?? (category === 'Other' ? t.other : category)
}

export function formatStreamTitle(stream, t) {
  return t.streamTitles?.[stream.title] ?? stream.title
}

export function formatEventLabel(label, t) {
  return t.eventLabels?.[label] ?? label
}

export function formatStatusLabel(status, t) {
  return t.statusLabels?.[status] ?? status
}

export function formatSummaryValue(value, t) {
  return t.summaryValues?.[value] ?? value
}
